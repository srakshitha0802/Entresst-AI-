export interface StorageService {
  saveData(collection: string, data: any): Promise<any>;
  getData(collection: string, id?: string): Promise<any>;
  updateData(collection: string, id: string, data: any): Promise<any>;
  deleteData(collection: string, id: string): Promise<any>;
  uploadFile(file: Buffer, filename: string, contentType: string): Promise<string>;
}

// In-memory storage - SHARED across all backends
const memoryStore = new Map<string, any[]>();

async function saveToMemory(collection: string, data: any): Promise<any> {
  const key = `storage_${collection}`;
  const existing = memoryStore.get(key) || [];
  const id = data.id || `mem_${Date.now()}_${Math.random().toString(36).substr(2, 9)}`;
  const item = { id, ...data };
  memoryStore.set(key, [...existing, item]);
  return item;
}

async function getFromMemory(collection: string, id?: string): Promise<any> {
  const key = `storage_${collection}`;
  const data = memoryStore.get(key) || [];
  if (id) {
    return data.find((item: any) => item.id === id) || null;
  }
  return data;
}

const memoryStorage: StorageService = {
  async saveData(collection: string, data: any) {
    return saveToMemory(collection, data);
  },
  async getData(collection: string, id?: string) {
    return getFromMemory(collection, id);
  },
  async updateData(collection: string, id: string, data: any) {
    const key = `storage_${collection}`;
    const existing = memoryStore.get(key) || [];
    const index = existing.findIndex((item: any) => item.id === id);
    if (index >= 0) {
      existing[index] = { ...existing[index], ...data };
      memoryStore.set(key, existing);
      return existing[index];
    }
    throw new Error('Item not found');
  },
  async deleteData(collection: string, id: string) {
    const key = `storage_${collection}`;
    const existing = memoryStore.get(key) || [];
    memoryStore.set(key, existing.filter((item: any) => item.id !== id));
    return true;
  },
  async uploadFile() {
    throw new Error('File upload not available');
  }
};

export class SupabaseService implements StorageService {
  private client: SupabaseClient;

  constructor(client: SupabaseClient) {
    this.client = client;
  }

  async saveData(collection: string, data: any) {
    if (!this.client) throw new Error('Supabase not configured');
    
    try {
      const { data: result, error } = await this.client
        .from(collection)
        .insert([data])
        .select();
    
      if (error) {
        console.log(`Supabase save failed for ${collection}, using memory:`, error.message);
        return saveToMemory(collection, data);
      }
      return result[0] || saveToMemory(collection, data);
    } catch (e) {
      return saveToMemory(collection, data);
    }
  }

  async getData(collection: string, id?: string) {
    if (!this.client) throw new Error('Supabase not configured');

    // First check memory
    const memData = await getFromMemory(collection, id);
    if (memData && (id ? true : memData.length > 0)) {
      return memData;
    }
    
    // Then try Supabase
    try {
      if (id) {
        const { data, error } = await this.client
          .from(collection)
          .select('*')
          .eq('id', id)
          .single();
        return data || null;
      } else {
        const { data, error } = await this.client.from(collection).select('*');
        return data || [];
      }
    } catch (error) {
      return id ? null : [];
    }
  }

  async updateData(collection: string, id: string, data: any) {
    if (!this.client) throw new Error('Supabase not configured');
    try {
      const { data: result, error } = await this.client
        .from(collection)
        .update(data)
        .eq('id', id)
        .select();
      return error ? { id, ...data } : (result[0] || { id, ...data });
    } catch (e) {
      return { id, ...data };
    }
  }

  async deleteData(collection: string, id: string) {
    if (!this.client) throw new Error('Supabase not configured');
    try {
      await this.client.from(collection).delete().eq('id', id);
      return true;
    } catch (e) {
      return true;
    }
  }

  async uploadFile() {
    throw new Error('File upload not available');
  }
}

export class FirebaseService implements StorageService {
  private db: admin.firestore.Firestore;
  private storage: admin.storage.Storage;

  constructor(db: admin.firestore.Firestore, storage: admin.storage.Storage) {
    this.db = db;
    this.storage = storage;
  }

  async saveData(collection: string, data: any) {
    const docRef = await this.db.collection(collection).add({
      ...data,
      createdAt: admin.firestore.FieldValue.serverTimestamp()
    });
    return { id: docRef.id, ...data };
  }

  async getData(collection: string, id?: string) {
    if (id) {
      const doc = await this.db.collection(collection).doc(id).get();
      return doc.exists ? { id: doc.id, ...doc.data() } : null;
    }
    const snapshot = await this.db.collection(collection).get();
    return snapshot.docs.map(doc => ({ id: doc.id, ...doc.data() }));
  }

  async updateData(collection: string, id: string, data: any) {
    await this.db.collection(collection).doc(id).update(data);
    return { id, ...data };
  }

  async deleteData(collection: string, id: string) {
    await this.db.collection(collection).doc(id).delete();
    return true;
  }

  async uploadFile() {
    throw new Error('File upload not available');
  }
}

